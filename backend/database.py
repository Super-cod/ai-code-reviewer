def save_analysis(
    source_url: str,
    owner: str,
    repo: str,
    repo_name: str,
    files_analyzed: int,
    report_text: str,
    confidence_score: Optional[float] = None,
    overall_score: Optional[float] = None,
    pr_number: Optional[int] = None,
    pr_title: Optional[str] = None,
    pr_author: Optional[str] = None,
) -> Optional[int]:
    with SessionLocal() as session:
        try:
            record = CodeAnalysis(
                source_url=source_url,
                owner=owner,
                repo=repo,
                repo_name=repo_name,
                pr_number=pr_number,
                pr_title=pr_title,
                pr_author=pr_author,
                files_analyzed=files_analyzed,
                report_text=report_text,
                confidence_score=confidence_score,
                overall_score=overall_score,
                analyzed_at=datetime.now(timezone.utc),
            )
            session.add(record)
            session.commit()
            session.refresh(record)
            logger.info("Analysis saved: id=%s  %s", record.id, repo_name)
            return record.id
        except Exception as exc:
            session.rollback()
            logger.error("Failed to save analysis: %s", exc)
            return None


def get_analysis_history(limit: int = 20, skip: int = 0) -> List[Dict[str, Any]]:
    with SessionLocal() as session:
        try:
            rows = (
                session.query(CodeAnalysis)
                .order_by(CodeAnalysis.analyzed_at.desc())
                .offset(skip).limit(limit).all()
            )
            return [r.to_dict() for r in rows]
        except Exception as exc:
            logger.error("Failed to fetch history: %s", exc)
            return []


def count_analyses() -> int:
    with SessionLocal() as session:
        try:
            return session.query(CodeAnalysis).count()
        except Exception:
            return 0


def get_analysis_by_id(analysis_id: int) -> Optional[Dict[str, Any]]:
    with SessionLocal() as session:
        try:
            row = session.query(CodeAnalysis).filter(CodeAnalysis.id == analysis_id).first()
            return row.to_dict() if row else None
        except Exception:
            return None


# Backward-compatible aliases expected by tests/docs from earlier API version.
def save_review(
    owner: str,
    repo: str,
    pr_number: int,
    pr_title: str,
    pr_author: str,
    review_text: str,
    confidence_score: Optional[float] = None,
    overall_score: Optional[float] = None,
) -> Optional[int]:
    return save_analysis(
        source_url=f"https://github.com/{owner}/{repo}/pull/{pr_number}",
        owner=owner,
        repo=repo,
        repo_name=f"{owner}/{repo}",
        pr_number=pr_number,
        pr_title=pr_title,
        pr_author=pr_author,
        files_analyzed=0,
        report_text=review_text,
        confidence_score=confidence_score,
        overall_score=overall_score,
    )


def get_review_history(limit: int = 20, skip: int = 0) -> List[Dict[str, Any]]:
    return get_analysis_history(limit=limit, skip=skip)


def count_total_reviews() -> int:
    return count_analyses()


def get_reviews_for_repo(owner: str, repo: str, limit: int = 20, skip: int = 0) -> List[Dict[str, Any]]:
    with SessionLocal() as session:
        try:
            rows = (
                session.query(CodeAnalysis)
                .filter(CodeAnalysis.owner == owner, CodeAnalysis.repo == repo)
                .order_by(CodeAnalysis.analyzed_at.desc())
                .offset(skip)
                .limit(limit)
                .all()
            )
            return [r.to_dict() for r in rows]
        except Exception as exc:
            logger.error("Failed to fetch repo reviews: %s", exc)
            return []
